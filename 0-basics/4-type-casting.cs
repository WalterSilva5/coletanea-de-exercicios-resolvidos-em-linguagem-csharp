using System;

namespace TypeConversionApplication {
  class StringConversion {
    static void Main(string[] args){
      int i = 10;
      float f = 1.4f;
      double d = 132.2;
      bool b = true;

      Console.WriteLine(i.ToString());
      Console.WriteLine(f.ToString());
      Console.WriteLine(d.ToString());
      Console.WriteLine(b.ToString());
    }
  }
}
